public class ServiceSubnamesBean {
	
	private int serviceId;
	private int subMasterId;
	private String subMasterName;
	private String subMasterUrl;
	private String activeFlag;
	/**
	 * @return the serviceId
	 */
	public int getServiceId() {
		return serviceId;
	}
	/**
	 * @param serviceId the serviceId to set
	 */
	public void setServiceId(int serviceId) {
		this.serviceId = serviceId;
	}
	/**
	 * @return the subMasterId
	 */
	public int getSubMasterId() {
		return subMasterId;
	}
	/**
	 * @param subMasterId the subMasterId to set
	 */
	public void setSubMasterId(int subMasterId) {
		this.subMasterId = subMasterId;
	}
	/**
	 * @return the subMasterName
	 */
	public String getSubMasterName() {
		return subMasterName;
	}
	/**
	 * @param subMasterName the subMasterName to set
	 */
	public void setSubMasterName(String subMasterName) {
		this.subMasterName = subMasterName;
	}
	/**
	 * @return the subMasterUrl
	 */
	public String getSubMasterUrl() {
		return subMasterUrl;
	}
	/**
	 * @param subMasterUrl the subMasterUrl to set
	 */
	public void setSubMasterUrl(String subMasterUrl) {
		this.subMasterUrl = subMasterUrl;
	}
	/**
	 * @return the activeFlag
	 */
	public String getActiveFlag() {
		return activeFlag;
	}
	/**
	 * @param activeFlag the activeFlag to set
	 */
	public void setActiveFlag(String activeFlag) {
		this.activeFlag = activeFlag;
	}
	
	
}
